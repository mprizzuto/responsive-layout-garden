		<blockquote cite="https://www.genome.gov/genetics-glossary/Mutation">
			<h3>variety is the spice of life</h3>

			<p>A mutation is a change in the DNA sequence of an organism. Mutations can result from errors in DNA replication during cell division, exposure to mutagens or a viral infection.</p>

			<p>Germline mutations (that occur in eggs and sperm) can be passed on to offspring, while somatic mutations (that occur in body cells) are not passed on.</p>

			<p>Mutation has been the source of many Hollywood movies, but it's really a simple process of a mistake made in a DNA sequence as it's being copied.</p>

			<p> Some of that's just the background noise that DNA copying is not perfect, and we should be glad of that or evolution couldn't operate. </p>

			<p>But mutation can also be induced by things like radiation or carcinogens in a way that can increase the risk of cancers or birth defects. But it's pretty simple; it's basically an induced misspelling of the DNA sequence. That's a mutation.</p>

			<span class="science-source">national Human Genome Research institute</span>
